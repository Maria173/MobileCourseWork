package com.example.kursworkapplication.operator.reportsOperator;

public class allOperatorsUnit {
    private String login;
    private String role;
    private int toursCount;
    private int guidesCount;
    private int stopsCount;

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public int getToursCount() {
        return toursCount;
    }

    public void setToursCount(int toursCount) {
        this.toursCount = toursCount;
    }

    public int getGuidesCount() {
        return guidesCount;
    }

    public void setGuidesCount(int guidesCount) {
        this.guidesCount = guidesCount;
    }

    public int getStopsCount() {
        return stopsCount;
    }

    public void setStopsCount(int stopsCount) {
        this.stopsCount = stopsCount;
    }
}
